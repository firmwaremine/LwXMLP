/*! \enum LwXMLP_enumEncodingTypeType
    \brief the type of supported encoding scheme
*/
typedef enum
{
    csEncoding_ERROR,       /*!< No char encoding detected */
    csEncoding_NONE,        /*!< No char encoding detected */
    csEncoding_UTF8,        /*!< UTF-8 */
    csEncoding_UTF16LE,     /*!< UTF-16 little endian */
    csEncoding_UTF16BE,     /*!< UTF-16 big endian */
    csEncoding_ASCII,       /*!< pure ASCII */
    csEncoding_UCS4LE,      /*!< UCS-4 little endian */
    csEncoding_UCS4BE,      /*!< UCS-4 big endian */
    csEncoding_EBCDIC,      /*!< EBCDIC uh! */
    csEncoding_UCS4_2143,   /*!< UCS-4 unusual ordering */
    csEncoding_UCS4_3412,   /*!< UCS-4 unusual ordering */
    csEncoding_UCS2,        /*!< UCS-2 */
    csEncoding_8859_1,      /*!< ISO-8859-1 ISO Latin 1 */
    csEncoding_8859_2,      /*!< ISO-8859-2 ISO Latin 2 */
    csEncoding_8859_3,      /*!< ISO-8859-3 */
    csEncoding_8859_4,      /*!< ISO-8859-4 */
    csEncoding_8859_5,      /*!< ISO-8859-5 */
    csEncoding_8859_6,      /*!< ISO-8859-6 */
    csEncoding_8859_7,      /*!< ISO-8859-7 */
    csEncoding_8859_8,      /*!< ISO-8859-8 */
    csEncoding_8859_9,      /*!< ISO-8859-9 */
    csEncoding_2022_JP,     /*!< ISO-2022-JP */
    csEncoding_SHIFT_JIS,   /*!< Shift_JIS */
    csEncoding_EUC_JP      /*!< EUC-JP */
}LwXMLP_enumEncodingTypeType;


